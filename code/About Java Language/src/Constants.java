public class Constants {
    public static void main(String[] args) {
        //constants

        int a = 24;
        a =32;
        //in this case the value of a is not constant since its changing
        // To make the value of a constant we use "final" keyword before initialising
        final double b = 3.14;     //double is used for floats (decimal numbers)
        //  b = 3.2;                 will show us an error


    }
}
